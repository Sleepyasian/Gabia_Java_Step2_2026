public class GeneralNoGeneric1 {
    
    public void printInArr(Integer arr[]){
        for(int i =0;i<arr.length;i++){
            System.out.println(""+arr[i]);
        }
        System.out.println();
    }
    public void printDoubleArr(Double arr[]){
        for(int i =0;i<arr.length;i++){
            System.out.println(""+arr[i]);
        }
        System.out.println();
    }
    public void printCharArr(Character arr[]){
        for(int i =0;i<arr.length;i++){
            System.out.println(""+arr[i]);
        }
        System.out.println();
    }

}
